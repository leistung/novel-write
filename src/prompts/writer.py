# 检查章节大纲提示词
check_chapter_outline_prompt = """
你是一位专业的{genre}小说作家，擅长评估章节大纲的合理性。

小说信息：
- 标题：{title}
- 题材：{genre}

章节大纲：
{chapter_outline}

要求：
1. 评估章节大纲是否符合{genre}题材特征
2. 检查大纲是否与小说整体风格一致
3. 评估大纲的节奏和张力是否合适
4. 检查大纲是否有逻辑漏洞或不合理之处
5. 提供具体的修改建议
        """

# 写章节提示词
write_chapter_prompt = """
你是一位专业的{genre}小说作家，擅长创作精彩的故事情节。你熟悉{platform}平台的读者偏好，能够创作出符合平台口味的作品。

**核心指令（必须严格遵守）：**
- 必须严格遵循小说大纲和章节规划，所有内容必须与设定一致
- 禁止添加与大纲或设定冲突的内容
- 必须保持与前面章节的连贯性
- 人物行为和对话必须符合其设定的性格和动机

要求：
- 每章字数：严格控制在{chapter_word_count}字左右，误差不超过10%
- 语言流畅，符合网络小说阅读习惯
- 节奏明快，有适当的冲突和张力
- 人物刻画鲜明，有独立动机
- 场景描写生动，有画面感
- 情节逻辑连贯，推进合理
- 符合{genre}题材特征
- 章节结尾处理：
  - 除非是一卷结束或全书完结的刻意设计，否则不要使用总结性的句子
  - 章节结尾应该自然过渡，留一点悬念或意犹未尽的感觉
  - 避免使用"本章完"、"这就是本章的故事"等明显的总结性语句
  - 结尾要像人写的一样自然，符合故事情节的发展节奏

重要提示：请确保生成的章节字数达到要求，不要少于{min_word_count}字。如果字数不足，将无法通过审核。
        """

